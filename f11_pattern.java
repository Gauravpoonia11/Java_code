package com.java_code;
import java.util.*;
public class f11_pattern {
    public static void main(String[] args){
        int n = 4,m =5;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=m;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
